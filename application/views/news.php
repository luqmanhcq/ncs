				<div class="entry-content">
					<p>
						Kian waktu, NCS sudah semakin dikenal masyarakat sebagai mitra jasa pengiriman yang dapat diandalkan. Kecepatan waktu dan tarifnya yang ekonomis menjadi indikator mengapa mereka mempercayakan NCS dibanding jasa pengiriman lain.
					</p>
					<p>
						Ini terbukti dengan bertambahnya jumlah agen NCS sepanjang tahun 2016 sebanyak 141 agen. Otomatis, semakin banyaknya jumlah agen, semakin banyak pula konsumen yang menggunakan jasa NCS.
					</p>
					<p>
						Bagi Anda yang masih ragu untuk beralih menggunakan jasa NCS, Anda tidak sendiri. Banyak orang masih bertanya-tanya, apa sih NCS itu? Bagaimana pelayanannya? Supaya Anda tidak semakin bingung, langsung saja simak ulasan mengapa Anda harus memilih NCS dibanding jasa pengiriman lain.
					</p>
					<ol>
						<li>
							<strong>Pengiriman Cepat</strong>
						</li>
					</ol>
					<p>
						Kecepatan waktu menjadi unsur paling utama mengapa konsumen lebih memilih NCS. Banyak orang tidak sabar agar paketnya lekas sampai di tujuan. Mengapa NCS lebih cepat, jawabannya adalah jaringan NCS yang sudah tersebar tidak hanya di kota besar, tapi juga sudah menjangkau kabupaten. Ini berkat pengalaman NCS selama 23 tahun untuk tidak lekas berpuas diri dengan terus memperluas jaringan kerja ke seluruh pelosok nusantara.
					</p>
					<ol start="2">
						<li>
							<strong>Biaya Lebih Ekonomis</strong>
						</li>
					</ol>
					<p>
						Bila ada jasa pengiriman yang sudah teruji kecepatannya dengan harga ekonomis, buat apa mencari yang lebih mahal? NCS dikenal lebih murah dibanding jasa pengiriman lain yang sudah memiliki nama. Selisih harga tentu berpengaruh bagi UKM dan online shop, bila sehari ada 20 paket saja katakan dengan selisih masing-masing sebesar 2.000, berapa besar pengeluaran yang bisa dihemat selama 30 hari penuh?
					</p>
					<ol start="3">
						<li>
							<strong>Sudah Ekonomis, Diskon dan Dapat Hadiah Lagi!</strong>
						</li>
					</ol>
					<p>
						Ini berlaku bagi customer yang mengikuti program Membership NCS bernama &#8220;NCS Loyalti Program&#8221;. Customer yang menggunakan kartu membership tiap melakukan pengiriman barang, akan langsung mendapat potongan diskon pengiriman sebesar 10% ditambah poin yang bila dikumpulkan terus menerus bisa ditukarkan dengan hadiah. Perhitungan tiap poin adalah customer akan mendapat 1 poin tiap melakukan transaksi sebesar Rp 5000,-. Jadi bila customer melakukan transaksi sebesar Rp 100.000,-, konsumen sudah mendapat 20 poin.
					</p>
					<p>
						Bagi customer yang ingin mendaftar program membership NCS dapat mengisi form di <a href="http://mitrausahancs.co.id/form-registrasi-ncs-loyalty-program/">Membership NCS</a>. Tidak dikenakan biaya apapun bagi customer yang ingin mendaftar program ini.
					</p>
					<ol start="4">
						<li>
							<strong>Keamanan Barang Terjamin</strong>
						</li>
					</ol>
					<p>
						Cepat,murah, diskon, dan dapat hadiah. Bagaimana dengan keamanan paket kita? Tenang, Anda akan mendapat status paket secara real time melalui website <a href="../cek-resi-ncs/index.html">NCS</a>. Report barang akan tertera dari mulai dientry, disortir, pengiriman ke kota tujuan, tiba di warehouse, sampai dalam proses pengiriman oleh kurir. Sehingga bila terjadi apa-apa, bisa ditelusuri dan diinvestigasi.
					</p>
					<p>
						Dan bila akhirnya terjadi hal-hal yang tidak diinginkan seperti kehilangan, NCS sudah otomatis memberikan penggantian sebesar 10 kali biaya kirim. Tentu tidak masalah bila barang yang dikirim nilainya dibawah 10 kali biaya kirim. Bila barang tsb harganya di atas 10 kali biaya kirim, baiknya langsung Anda asuransikan sebesar nilai barang ybs untuk mengantisipasi terjadinya kehilangan.
					</p>
					<ol start="5">
						<li>
							<strong>Layanan Pick Up</strong>
						</li>
					</ol>
					<p>
						Customer yang memiliki jumlah pengiriman besar setiap harinya seperti online shop tidak perlu repot mendatangi agen terdekat untuk mengirim paket. Minimal pengiriman 5-10 paket setiap harinya, agen NCS akan dengan sukarela menjemput paket Anda. Dan nomor resi akan diberikan via email atau whatsapp pada hari itu juga. Dengan begitu, Anda dapat menghemat waktu dengan tidak perlu mendatangi agen dan mengikuti antrian.
					</p>
					<ol start="6">
						<li>
							<strong>Support Live Chat Customer Service</strong>
						</li>
					</ol>
					<p>
						Selancar-lancarnya proses pengiriman barang, tentu ada kondisi di mana <em>human error</em>atau <em>traffic</em>seperti bencana alam terjadi hingga menyebabkan paket Anda mengalami keterkendalaan. Untuk itu Anda tidak perlu khawatir. NCS sudah bersiap akan hal ini. Anda tidak perlu repot menelepon customer service pusat. Cukup menghubungi via <a href="http://mitrausahancs.co.id/fitur-live-chat-kini-sudah-tersedia-di-website-ncs/">live chat</a> dengan mencantumkan nomor resi pengiriman, nama penerima, dan alamat lengkap penerima beserta patokan-patokannya. Secepatnya pihak NCS akan merespons dan meneruskan ke cabang terkait agar paket dapat segera di <em>speed up</em>.
					</p>
					<p>
						Itulah alasan-alasan mengapa kini banyak orang yang beranjak menggunakan NCS dibanding jasa pengiriman lain. So, bagi Anda yang memiliki bisnis online, tidak perlu ragu lagi untuk menitipkan paket Anda kepada NCS. Selain ketepatan waktu dan keamanan barang Anda terjamin, ada benefit khusus yang bisa Anda dapatkan melalui program membership NCS. Kami tunggu untuk menjadi bagian dari keluarga besar NCS! <img src="../wp-includes/images/smilies/simple-smile.png" alt=":)" class="wp-smiley" style="height: 1em; max-height: 1em;"/>
					</p>
